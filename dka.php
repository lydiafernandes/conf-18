Dr. Agrawal has been elected to the membership of the Alpha Omega Alpha Honor Medical Society and is a Fellow of the American Academy of Allergy, Asthma and Immunology, Fellow of the American Heart Association, Fellow of the American Physiological Society, and Fellow of the International Academy of Cardiovascular Sciences. Creighton University recognized and honored Dr. Agrawal with many awards, including Young Investigator Award, Distinguished Research Career Award, Distinguished Professor Award, Distinguished Faculty Service Award, University Research Award and Distinguished Mentor Award at Creighton. In May 2017, Dr. Agrawal was honored with the highest teaching award at Creighton University, the Robert F. Kennedy Memorial Award for Teaching Achievement. In 2001 and again in 2017, the Midwest Chapter of American Association of Physicians of India recognized and honored Dr. Agrawal for his contributions as a teaching professor, researcher, and mentor. The India Association of Nebraska recognized him with the Man of the Year award in 2000 and Award of Excellence in appreciation of outstanding service and contribution to Nebraska Community at large in 2013. In 2015 and 2016, the International Academy of Cardiovascular Sciences recognized Dr. Agrawal with the Professor Bohuslav Ostadal Award for Excellence in Cardiovascular Sciences, Distinguished Leadership Award, and Ricardo Gelpi Award for Excellence in Cardiovascular Sciences. Dr. Agrawal has been an invited speaker to many prestigious national and international scientific meetings.
<br><br>
