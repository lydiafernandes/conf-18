     Dr. Shubha Tole is Professor of Neurobiology and Principal Investigator in Department of Biological Sciences, Tata Institute of Fundamental Research in Mumbai, India. Her work is in developmental neuroscience, and for the last few decades, she has been working to elucidate the mechanisms behind the development and diseases of certain brain structures. 
<br><br>
Her most significant contribution to the scientific world has been her discovery of the regulatory gene, Lhx2, that controls some aspects of how the amygdala, cortex, and the hippocampus form during early development winning the Infosys Prize in the Life Sciences category in 2014. 
<br><br>
Dr. Shubha Tole obtained her MS and PhD degree from California Institute of Technology (Caltech) and post-doctoral research at University of Chicago. 
<br><br>Dr. Shubha Tole is a member of International Affairs Committee of the American Society for Cell Biology. She is also a Fellow of the National Academy of Sciences, India and the Indian Academy of Sciences. 
<br><br>Dr. Tole has been a recipient of various prestigious awards such as Wellcome Trust Senior International Fellowship (1999), the Swarnajayanti Fellowship from the Department of Science and Technology (DST), Government of India (2005), the National Woman Bioscientist award from the Department of Biotechnology (DBT), Government of India (2008), The Research Award for Innovation in Neurosciences (RAIN award) from the Society for Neuroscience, United States (2008), and the Shanti Swarup Bhatnagar Award (2010).[  She was also awarded the Wellcome Trust Flexible Travel Award grant by Stanford University for a Sabbatical year in 2008, which she took with her family.
<br><br>In 2014, Dr. Tole was awarded the Infosys Prize of 55 lakh rupees for her work done to elucidate the mechanisms and genes that are involved in the formation of the hippocampus.
<br><br>Dr. Tole has more than 50 research publications to her credit with publications in esteemed Journals like Science, Nature Neuroscience, PNAS, Cerebral Cortex, Cellular and Molecular Life science. She has also written an editorial in the journal ‘Science’ and written a chapter Telencephalon Patterning. “Cellular Migration and Formation of Neuronal Connections:  Comprehensive Developmental Neuroscience” (2013) John Rubenstein and PaskoRakic (Eds).  Academic Press.  ISBN: 978-0-12-397265-1 
