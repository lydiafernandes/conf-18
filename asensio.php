     Dr. Asensio is a native of Havana, Cuba.  At the age of 13, he immigrated to Chicago, Illinois, where he completed his undergraduate education with a double major at the University of Illinois, and subsequently went on to Rush Medical College of Rush University where he completed the requirements for his medical degree.  He completed his surgical residency at Northwestern University in Chicago and undertook further post-graduate surgical training at the Medical College of Ohio.  He then completed a fellowship in Trauma Surgery, and Surgical Critical Care at the University of Texas Health Sciences Center/Southwestern Medical School-Parkland Memorial Hospital in Dallas Texas.  His specialty is Surgery and his sub–specialties are Trauma Surgery and Surgical Critical Care.  He is double board certified in Surgery and Surgical Critical Care and is a fellow of the American College of Surgeons.
<br><br>
Dr. Asensio currently serves as Professor and Vice-Chairman of the Department of Surgery of Creighton University (With Tenure).  He has been tenured at four other universities.  He is also the Chief of the Division of Trauma Surgery and Surgical Care.  He is also the Director of the Trauma Center and Trauma Program at Creighton University Hospital.  He is the Professor of Clinical & Translational Science Creighton University Medical School. He is also appointed as Professor of Surgery Adjunct, Uniform Services University of the Health Sciences (USUHS), F. Edward Hebert School of Medicine, Bethesda, Maryland.  
<br><br>
Dr. Asensio is a member of the Alpha Omega Alpha (AOA) Honorary Medical Fraternity. He served as Vice-Chairman of the Committee on Trauma of the Southern California Chapter of the American College of Surgeons and as such, he has participated in the National Committee on Trauma of the American College of Surgeons.  He was a member of the International Relations Committee of the American College of Surgeons where he held the position of Chairman of the International Scholars Selection Committee for two years.  He holds membership in more than 81 professional Societies and is currently a Fellow of the American College of Surgeons (FACS) and a Fellow of the American College of Clinical Care Medicine (ACCM), a Fellow of the American Association for the Surgery of Trauma (AAST), where he has participated in multiple committees and Chaired the International Relations Committee of this Society.  He is a Charter Member of both, the Eastern Association for the Surgery of Trauma (EAST) and the Panamerican Trauma Society (PTS).  He is a member of the International Association for the Surgery of Trauma and Intensive Care (IATSIC), the American Burn Association (ABA), the Association for Academic Surgeons (AAS), the Society of University Surgeons (SUS), the Society of Critical Care Medicine (SCCM), International Surgical Society (ISS), Western Surgical Association (WSA), Southern Surgical Association (SSA), Central Surgical Association (CSA),  Pacific Coast Surgical Association (PCSA), Association for Surgical Education (ASE) as well as the Society for Surgery of the Alimentary Tract (SSAT), Southwestern Surgical Congress (SWSC), Southeastern Surgical Congress (SESC), International Society for Digestive Surgery (ISDS), Surgical Infection Society (SIS), the American Association for Vascular Surgery (AAVS), Society for Vascular Surgery (SVS), the Society for Clinical Vascular Surgery, the International Society for Cardiovascular Surgery (ISCVS), the Eastern Vascular Society (EVS), New England Vascular Society (NEVS), 
Shock Society, the Ambrose Pare and the Excelsior Surgical Societies, and many other prestigious 
surgical societies.  He is also an Honorary Member of many International Surgical, Trauma Surgical 
and Critical Care Societies from Europe, North, Central South America and Asia. He has received Honorary Fellowship from The Royal College of Surgeons of England. He is an honorary member of the Royal Spanish Academy of Medicine as well as the Argentinean and Mexican Academies of Surgery. 
<br><br>
Dr. Asensio is both, a national and an internationally recognized expert in the field of Trauma Surgery.  His area of focused study and expertise is surgical techniques and outcomes for Difficult Injuries and Difficult Problems in Trauma Surgery.  His main interests are cardiovascular, thoracic, and complex abdominal and peripheral vascular injuries.  He has published extensively in these areas.  He has a total of 529 publications including peer-reviewed articles, book chapters and 22 books and monographs including 4 issues of the Surgical Clinics of North America and one issue of the Emergency Medicine Clinics of North America. His h-Index is 56. He has lectured extensively nationally and internationally in Europe, North, Central and South America, Asia, Africa and Australia and New Zealand and has been an invited Visiting Professor at many prestigious institutions.  He has received multiple teaching awards from medical students, residents , fellows and surgical societies nationally and internationally.  His expertise and operative skills have been the subject of many documentaries and programs including a 60 Minutes program with Dan Rather, a two hour documentary about his life on the Discovery Health Channel titled “Medical Maverick” as well as another Discovery Special titled “Surgery Saved My Life - Battlefield Miami”.


